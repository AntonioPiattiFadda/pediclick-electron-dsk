import { useOrderContext } from "../../context/OrderContext";

const CurrentOrderItem = () => {
  const { currentOrderItem, setOrderItems } = useOrderContext();
  return (
    <div>
      {currentOrderItem?.product_name}
      <button
        onClick={() => setOrderItems((prev) => [...prev, currentOrderItem])}
      >
        Agregar
      </button>
    </div>
  );
};

export default CurrentOrderItem;
